package outros;

public class ComandoDeSelecao {

	public static void main(String[] args) {

		int escolha = 1;
		switch(escolha) {
		case 1:
			System.out.println("Número 1");
			break;
		case 2:
			System.out.println("Número 2");
			break;
		case 3:
			System.out.println("Número 3");
			break;	
		case 4:
			System.out.println("Número 4");
			break;	
			
			default:
				System.out.println("Outro numero");
		}

	}

}
