package outros;

public class OperadoresdeAtribuicaoCompostos {

	public static void main(String[] args) {
		
		int numero = 10;
		
		numero += 3;
		System.out.println(numero);

		numero -= 2;
		System.out.println(numero);
		
		numero *= 4;
		System.out.println(numero);
	
		numero /= 2;
		System.out.println(numero);
	}

}
